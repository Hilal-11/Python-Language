
# Lamda functions in python 

# lambda arguments : expression
    # a lamda function is a small anonomous function , that takes number of argumants and have only 
    # single/one expression
# lambda x , y : x + y

add = lambda x , y : x + y
print(add(10 , 20))

mul = lambda x , y , z : x*y*z
print(mul(10 , 6 , 2))


